    private void enterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonActionPerformed
        // TODO add your handling code here:
        double result = 0;
        
        String inputToSolve = displayText.getText();
        createSubstring(inputToSolve, ' ', 2);
        String firstValue = sub1;
        String secondValue = sub2;
        
        switch(isWhatOperation){
            case "division":
                result = Double.parseDouble(firstValue) / Double.parseDouble(secondValue);
                break;
            case "multiplication":
                result = Double.parseDouble(firstValue) * Double.parseDouble(secondValue);
                break;
            case "subtraction":
                result = Double.parseDouble(firstValue) - Double.parseDouble(secondValue);
                break;
            case "addition":
                result = Double.parseDouble(firstValue) + Double.parseDouble(secondValue);
                break;
        }
        
        String resultToDisplay = Double.toString(result);
        createSubstring(resultToDisplay, '.', 1);
        String beforePoint = sub1;
        String afterPoint = sub2;
        
        
        if(afterPoint.equals("0")){
            displayText.setText(beforePoint);
        }else{
            displayText.setText(resultToDisplay);
        }
        
    }//GEN-LAST:event_enterButtonActionPerformed

