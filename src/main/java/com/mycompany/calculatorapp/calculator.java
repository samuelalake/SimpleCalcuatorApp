    public void createSubstring(String mainString, char separatingChar, int separatorCount){
        int separatorCounter = 0;
        sub1 = ""; sub2 = "";
        for(int i = 0; i < mainString.length(); i++){ 
            
            if(mainString.charAt(i) != separatingChar && separatorCounter == 0){
                sub1 += mainString.charAt(i);
            }else if(mainString.charAt(i) == separatingChar){
                separatorCounter++;
            }else if(separatorCounter == separatorCount){
                sub2 += mainString.charAt(i);
            }
            
        }
    }

